#!/usr/bin/env node
import {
  finalizeGeneration,
  readJson,
  resolvePaths,
  writeIfChanged,
} from './lib/generator-file-utils.mjs';

const {
  projectRoot,
  configPath,
  rustOutPath,
  jsOutPath,
} = resolvePaths(import.meta.url, {
  configPath: ['config', 'skill-taxonomy.json'],
  rustOutPath: ['rust', 'skill_taxonomy_generated.rs'],
  jsOutPath: ['public', 'skill-taxonomy.js'],
});

function fail(message) {
  throw new Error(message);
}

function validate(config) {
  if (!Number.isInteger(config?.version) || config.version < 1) {
    fail('version must be a positive integer');
  }
  if (!Array.isArray(config?.canonical_skills) || config.canonical_skills.length === 0) {
    fail('canonical_skills must be a non-empty array');
  }

  const ids = new Set();
  for (const skill of config.canonical_skills) {
    if (!skill || typeof skill !== 'object') {
      fail('canonical_skills entries must be objects');
    }
    const { id, label, badge_noun: badgeNoun, quest_indices: questIndices } = skill;
    if (typeof id !== 'string' || !/^[a-z][a-z-]*$/.test(id)) {
      fail(`invalid skill id: ${String(id)}`);
    }
    if (ids.has(id)) {
      fail(`duplicate skill id: ${id}`);
    }
    ids.add(id);
    if (typeof label !== 'string' || label.trim().length === 0) {
      fail(`label missing for skill: ${id}`);
    }
    if (typeof badgeNoun !== 'string' || badgeNoun.trim().length === 0) {
      fail(`badge_noun missing for skill: ${id}`);
    }
    if (!Array.isArray(questIndices) || questIndices.length === 0) {
      fail(`quest_indices must be a non-empty array for skill: ${id}`);
    }
    if (!questIndices.every((n) => Number.isInteger(n) && n >= 0)) {
      fail(`quest_indices must contain non-negative integers for skill: ${id}`);
    }
  }

  const aliases = config.aliases ?? {};
  if (typeof aliases !== 'object' || Array.isArray(aliases)) {
    fail('aliases must be an object');
  }
  for (const [alias, canonical] of Object.entries(aliases)) {
    if (typeof alias !== 'string' || !/^[a-z][a-z-]*$/.test(alias)) {
      fail(`invalid alias key: ${String(alias)}`);
    }
    if (!ids.has(canonical)) {
      fail(`alias target must be canonical skill: ${alias} -> ${String(canonical)}`);
    }
  }

  const thresholds = config.skill_mastery_thresholds;
  for (const tier of ['bronze', 'silver', 'gold', 'platinum']) {
    const value = thresholds?.[tier];
    if (!Number.isInteger(value) || value < 1) {
      fail(`skill_mastery_thresholds.${tier} must be a positive integer`);
    }
  }

  if (!(thresholds.bronze < thresholds.silver && thresholds.silver < thresholds.gold && thresholds.gold < thresholds.platinum)) {
    fail('skill mastery thresholds must be strictly increasing: bronze < silver < gold < platinum');
  }
}

function q(value) {
  return JSON.stringify(value);
}

function generateRust(config) {
  const canonicalSkills = config.canonical_skills;
  const aliasEntries = Object.entries(config.aliases ?? {});
  const labels = Object.fromEntries(canonicalSkills.map((s) => [s.id, s.label]));
  const badgeNouns = Object.fromEntries(canonicalSkills.map((s) => [s.id, s.badge_noun]));

  const canonicalMatchArms = canonicalSkills
    .map((s) => `        ${q(s.id)} => ${q(s.id)},`)
    .join('\n');
  const aliasMatchArms = aliasEntries
    .map(([alias, canonical]) => `        ${q(alias)} => ${q(canonical)},`)
    .join('\n');
  const labelArms = canonicalSkills
    .map((s) => `        ${q(s.id)} => ${q(labels[s.id])},`)
    .join('\n');
  const badgeNounArms = canonicalSkills
    .map((s) => `        ${q(s.id)} => ${q(badgeNouns[s.id])},`)
    .join('\n');
  const questArms = canonicalSkills
    .map((s) => `        ${q(s.id)} => &[${s.quest_indices.join(', ')}],`)
    .join('\n');

  return `// @generated by scripts/generate-skill-taxonomy.mjs. Do not edit manually.\n\n#[allow(dead_code)]\npub const SKILL_TAXONOMY_VERSION: u16 = ${config.version};\n\npub const CANONICAL_SKILLS: &[&str] = &[${canonicalSkills.map((s) => q(s.id)).join(', ')}];\n\n#[allow(dead_code)]\npub const SKILL_ALIASES: &[(&str, &str)] = &[${aliasEntries
    .map(([alias, canonical]) => `(${q(alias)}, ${q(canonical)})`)
    .join(', ')}];\n\npub const SKILL_MASTERY_BRONZE_THRESHOLD: u32 = ${config.skill_mastery_thresholds.bronze};\npub const SKILL_MASTERY_SILVER_THRESHOLD: u32 = ${config.skill_mastery_thresholds.silver};\npub const SKILL_MASTERY_GOLD_THRESHOLD: u32 = ${config.skill_mastery_thresholds.gold};\n#[allow(dead_code)]\npub const SKILL_MASTERY_PLATINUM_THRESHOLD: u32 = ${config.skill_mastery_thresholds.platinum};\n\npub fn canonicalize_skill(skill: &str) -> &'static str {\n    match skill {\n${canonicalMatchArms}\n${aliasMatchArms ? `${aliasMatchArms}\n` : ''}        _ => ${q(canonicalSkills[canonicalSkills.length - 1].id)},\n    }\n}\n\npub fn skill_label(skill: &str) -> &'static str {\n    match canonicalize_skill(skill) {\n${labelArms}\n        _ => ${q(labels[canonicalSkills[canonicalSkills.length - 1].id])},\n    }\n}\n\n#[allow(dead_code)]\npub fn skill_badge_noun(skill: &str) -> &'static str {\n    match canonicalize_skill(skill) {\n${badgeNounArms}\n        _ => ${q(badgeNouns[canonicalSkills[canonicalSkills.length - 1].id])},\n    }\n}\n\npub fn skill_quest_indices(skill: &str) -> &'static [usize] {\n    match canonicalize_skill(skill) {\n${questArms}\n        _ => &[],\n    }\n}\n`;
}

function generateJs(config) {
  const canonicalSkills = config.canonical_skills;
  const labels = Object.fromEntries(canonicalSkills.map((s) => [s.id, s.label]));
  const badgeNouns = Object.fromEntries(canonicalSkills.map((s) => [s.id, s.badge_noun]));
  const questIndices = Object.fromEntries(canonicalSkills.map((s) => [s.id, s.quest_indices]));

  return `// @generated by scripts/generate-skill-taxonomy.mjs. Do not edit manually.\nexport const SKILL_TAXONOMY_VERSION = ${config.version};\nexport const CANONICAL_SKILLS = Object.freeze(${q(canonicalSkills.map((s) => s.id))});\nexport const SKILL_ALIASES = Object.freeze(${q(config.aliases ?? {})});\nexport const SKILL_LABELS = Object.freeze(${q(labels)});\nexport const SKILL_BADGE_NOUNS = Object.freeze(${q(badgeNouns)});\nexport const SKILL_QUEST_INDICES = Object.freeze(${q(questIndices)});\nexport const SKILL_MASTERY_THRESHOLDS = Object.freeze(${q(config.skill_mastery_thresholds)});\n\nexport function toCanonicalSkill(skill) {\n  if (typeof skill !== 'string' || skill.length === 0) {\n    return CANONICAL_SKILLS[CANONICAL_SKILLS.length - 1];\n  }\n  if (CANONICAL_SKILLS.includes(skill)) {\n    return skill;\n  }\n  return SKILL_ALIASES[skill] || CANONICAL_SKILLS[CANONICAL_SKILLS.length - 1];\n}\n\nexport const SKILL_TAXONOMY = Object.freeze({\n  SKILL_TAXONOMY_VERSION,\n  CANONICAL_SKILLS,\n  SKILL_ALIASES,\n  SKILL_LABELS,\n  SKILL_BADGE_NOUNS,\n  SKILL_QUEST_INDICES,\n  SKILL_MASTERY_THRESHOLDS,\n});\n`;
}

function main() {
  const config = readJson(configPath);
  validate(config);

  const rustChanged = writeIfChanged(rustOutPath, generateRust(config));
  const jsChanged = writeIfChanged(jsOutPath, generateJs(config));

  finalizeGeneration(
    projectRoot,
    [
      { filePath: rustOutPath, changed: rustChanged },
      { filePath: jsOutPath, changed: jsChanged },
    ],
    'Skill taxonomy outputs are up-to-date',
  );
}

main();
