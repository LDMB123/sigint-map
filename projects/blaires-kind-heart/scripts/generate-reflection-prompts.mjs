#!/usr/bin/env node
import {
  finalizeGeneration,
  readJson,
  resolvePaths,
  writeIfChanged,
} from './lib/generator-file-utils.mjs';

const {
  projectRoot,
  configPath,
  jsOutPath,
} = resolvePaths(import.meta.url, {
  configPath: ['config', 'reflection-prompts.json'],
  jsOutPath: ['public', 'reflection-prompts.generated.js'],
});

function validate(config) {
  if (!Number.isInteger(config?.version) || config.version < 1) {
    throw new Error('version must be a positive integer');
  }
  if (!Array.isArray(config?.prompts) || config.prompts.length === 0) {
    throw new Error('prompts must be a non-empty array');
  }

  const ids = new Set();
  for (const prompt of config.prompts) {
    if (typeof prompt?.id !== 'string' || prompt.id.trim().length === 0) {
      throw new Error('prompt.id is required');
    }
    if (ids.has(prompt.id)) {
      throw new Error(`duplicate prompt id: ${prompt.id}`);
    }
    ids.add(prompt.id);

    for (const key of ['category', 'prompt_text', 'emoji']) {
      if (typeof prompt[key] !== 'string' || prompt[key].trim().length === 0) {
        throw new Error(`prompt ${prompt.id} missing ${key}`);
      }
    }

    if (!Array.isArray(prompt.response_options) || prompt.response_options.length < 2) {
      throw new Error(`prompt ${prompt.id} must have at least 2 response options`);
    }
  }
}

function generateJs(config) {
  return `// @generated by scripts/generate-reflection-prompts.mjs. Do not edit manually.\nexport const REFLECTION_PROMPT_VERSION = ${config.version};\nexport const REFLECTION_PROMPTS = Object.freeze(${JSON.stringify(config.prompts)});\n`;
}

function main() {
  const config = readJson(configPath);
  validate(config);

  const jsChanged = writeIfChanged(jsOutPath, generateJs(config));

  finalizeGeneration(
    projectRoot,
    [{ filePath: jsOutPath, changed: jsChanged }],
    'Reflection prompt outputs are up-to-date',
  );
}

main();
