#!/usr/bin/env node
import {
  finalizeGeneration,
  readJson,
  resolvePaths,
  writeIfChanged,
} from './lib/generator-file-utils.mjs';

const { projectRoot, configPath, rustOutPath, jsOutPath } = resolvePaths(import.meta.url, { configPath: ['config', 'db-contract.json'], rustOutPath: ['rust', 'db_contract_generated.rs'], jsOutPath: ['public', 'db-contract.js'] });

function readRequiredUInt(config, key) {
  const value = config?.[key];
  if (!Number.isInteger(value) || value < 1) {
    throw new Error(`${key} must be a positive integer`);
  }
  return value;
}

function generateRust({
  dbWorkerApiVersion,
  dbWorkerMinClientApiVersion,
  dbSchemaVersion,
  exportFormatVersion,
}) {
  return `// @generated by scripts/generate-db-contract.mjs. Do not edit manually.\n\npub const DB_WORKER_API_VERSION: u16 = ${dbWorkerApiVersion};\n#[allow(dead_code)]\npub const DB_WORKER_MIN_CLIENT_API_VERSION: u16 = ${dbWorkerMinClientApiVersion};\npub const DB_SCHEMA_VERSION: u16 = ${dbSchemaVersion};\npub const EXPORT_FORMAT_VERSION: u16 = ${exportFormatVersion};\n`;
}

function generateJs({
  dbWorkerApiVersion,
  dbWorkerMinClientApiVersion,
  dbSchemaVersion,
  exportFormatVersion,
}) {
  return `// @generated by scripts/generate-db-contract.mjs. Do not edit manually.\nexport const DB_WORKER_API_VERSION = ${dbWorkerApiVersion};\nexport const DB_WORKER_MIN_CLIENT_API_VERSION = ${dbWorkerMinClientApiVersion};\nexport const DB_SCHEMA_VERSION = ${dbSchemaVersion};\nexport const EXPORT_FORMAT_VERSION = ${exportFormatVersion};\n\nexport const DB_CONTRACT = Object.freeze({\n  DB_WORKER_API_VERSION,\n  DB_WORKER_MIN_CLIENT_API_VERSION,\n  DB_SCHEMA_VERSION,\n  EXPORT_FORMAT_VERSION,\n});\n`;
}

function main() {
  const config = readJson(configPath);
  const values = {
    dbWorkerApiVersion: readRequiredUInt(config, 'db_worker_api_version'),
    dbWorkerMinClientApiVersion: readRequiredUInt(config, 'db_worker_min_client_api_version'),
    dbSchemaVersion: readRequiredUInt(config, 'db_schema_version'),
    exportFormatVersion: readRequiredUInt(config, 'export_format_version'),
  };

  const rustContent = generateRust(values);
  const jsContent = generateJs(values);

  const rustChanged = writeIfChanged(rustOutPath, rustContent);
  const jsChanged = writeIfChanged(jsOutPath, jsContent);

  finalizeGeneration(projectRoot, [
    { filePath: rustOutPath, changed: rustChanged },
    { filePath: jsOutPath, changed: jsChanged },
  ], 'DB contract outputs are up-to-date');
}

main();
