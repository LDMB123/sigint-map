#!/usr/bin/env node
import {
  finalizeGeneration,
  readJson,
  resolvePaths,
  writeIfChanged,
} from './lib/generator-file-utils.mjs';

const { projectRoot, configPath, rustOutPath, jsOutPath } = resolvePaths(import.meta.url, { configPath: ['config', 'panels.json'], rustOutPath: ['rust', 'panel_registry_generated.rs'], jsOutPath: ['public', 'panel-registry.js'] });

function stableSortPanels(panels) {
  return [...panels].sort((a, b) => a.id.localeCompare(b.id));
}

function escapeRustString(value) {
  return value
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\n/g, '\\n');
}

function formatRustOption(value) {
  if (value === null || value === undefined) {
    return 'None';
  }
  return `Some("${escapeRustString(String(value))}")`;
}

function formatRustPanel(panel) {
  return [
    '    PanelMeta {',
    `        id: "${escapeRustString(panel.id)}",`,
    `        narration_phrase: ${formatRustOption(panel.narration_phrase)},`,
    `        first_visit_tip: ${formatRustOption(panel.first_visit_tip)},`,
    `        sound_key: ${formatRustOption(panel.sound_key)},`,
    '    },'
  ].join('\n');
}

function validatePanels(panels) {
  const seen = new Set();
  const byId = new Map(panels.map((p) => [p.id, p]));
  for (const panel of panels) {
    if (!panel.id || typeof panel.id !== 'string') {
      throw new Error('Each panel must have a string id');
    }
    if (seen.has(panel.id)) {
      throw new Error(`Duplicate panel id: ${panel.id}`);
    }
    seen.add(panel.id);
    if (!panel.breadcrumb || typeof panel.breadcrumb !== 'object') {
      throw new Error(`Panel ${panel.id} is missing breadcrumb object`);
    }
    for (const key of ['icon', 'label', 'aria']) {
      if (typeof panel.breadcrumb[key] !== 'string') {
        throw new Error(`Panel ${panel.id} breadcrumb.${key} must be a string`);
      }
    }
    for (const key of ['panel_title', 'panel_aria']) {
      if (typeof panel[key] !== 'string' || panel[key].length === 0) {
        throw new Error(`Panel ${panel.id} ${key} must be a non-empty string`);
      }
    }
    if (typeof panel.theme_token !== 'string' || panel.theme_token.length === 0) {
      throw new Error(`Panel ${panel.id} must have a non-empty theme_token`);
    }
    if (panel.parent !== null && panel.parent !== undefined && !byId.has(panel.parent)) {
      throw new Error(`Panel ${panel.id} has unknown parent ${panel.parent}`);
    }
  }
}

function generateRust(panels) {
  const body = panels.map(formatRustPanel).join('\n');
  return `// @generated by scripts/generate-panel-registry.mjs. Do not edit manually.\n\npub const PANEL_REGISTRY: &[PanelMeta] = &[\n${body}\n];\n`;
}

function generateJs(panels) {
  const map = Object.fromEntries(
    panels.map((panel) => [
      panel.id,
      {
        id: panel.id,
        parent: panel.parent ?? null,
        breadcrumb: panel.breadcrumb,
        panel_title: panel.panel_title,
        panel_aria: panel.panel_aria,
        theme_token: panel.theme_token,
      },
    ]),
  );
  return `// @generated by scripts/generate-panel-registry.mjs. Do not edit manually.\nexport const PANEL_REGISTRY = Object.freeze(${JSON.stringify(map, null, 2)});\nexport default PANEL_REGISTRY;\n`;
}

function main() {
  const config = readJson(configPath);
  const panels = stableSortPanels(config.panels || []);
  validatePanels(panels);

  const rustContent = generateRust(panels);
  const jsContent = generateJs(panels);

  const rustChanged = writeIfChanged(rustOutPath, rustContent);
  const jsChanged = writeIfChanged(jsOutPath, jsContent);

  finalizeGeneration(projectRoot, [
    { filePath: rustOutPath, changed: rustChanged },
    { filePath: jsOutPath, changed: jsChanged },
  ], 'Panel registry outputs are up-to-date');
}

main();
