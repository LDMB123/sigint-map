#!/usr/bin/env node
import {
  finalizeGeneration,
  readJson,
  resolvePaths,
  writeIfChanged,
} from './lib/generator-file-utils.mjs';

const {
  projectRoot,
  taxonomyPath,
  rustOutPath,
  jsOutPath,
} = resolvePaths(import.meta.url, {
  taxonomyPath: ['config', 'skill-taxonomy.json'],
  rustOutPath: ['rust', 'skill_mastery_badges_generated.rs'],
  jsOutPath: ['public', 'skill-mastery-badges.js'],
});

const TIER_ORDER = ['bronze', 'silver', 'gold', 'platinum'];
const TIER_EMOJI = {
  bronze: '🥉',
  silver: '🥈',
  gold: '🥇',
  platinum: '🏆',
};

function titleCase(value) {
  return value.charAt(0).toUpperCase() + value.slice(1);
}

function q(value) {
  return JSON.stringify(value);
}

function buildRows(config) {
  const thresholds = config.skill_mastery_thresholds;
  const rows = [];

  for (const skill of config.canonical_skills) {
    for (const tier of TIER_ORDER) {
      rows.push({
        id: `badge-${skill.id}-${tier}`,
        badge_type: 'skill_mastery',
        badge_name: `${titleCase(tier)} ${skill.badge_noun}`,
        emoji: TIER_EMOJI[tier],
        unlock_criteria: `${thresholds[tier]} ${skill.id} acts`,
        tier,
      });
    }
  }

  return rows;
}

function generateRust(rows) {
  const entries = rows
    .map(
      (row) => `    Badge {\n        id: ${q(row.id)},\n        badge_type: ${q(row.badge_type)},\n        badge_name: ${q(row.badge_name)},\n        emoji: ${q(row.emoji)},\n        unlock_criteria: ${q(row.unlock_criteria)},\n        tier: ${q(row.tier)},\n    },`
    )
    .join('\n');

  return `// @generated by scripts/generate-skill-mastery-badges.mjs. Do not edit manually.\n\npub const SKILL_MASTERY_BADGES: &[Badge] = &[\n${entries}\n];\n`;
}

function generateJs(rows) {
  return `// @generated by scripts/generate-skill-mastery-badges.mjs. Do not edit manually.\nexport const SKILL_MASTERY_BADGES = Object.freeze(${q(rows)});\n`;
}

function main() {
  const config = readJson(taxonomyPath);
  const rows = buildRows(config);

  const rustChanged = writeIfChanged(rustOutPath, generateRust(rows));
  const jsChanged = writeIfChanged(jsOutPath, generateJs(rows));

  finalizeGeneration(
    projectRoot,
    [
      { filePath: rustOutPath, changed: rustChanged },
      { filePath: jsOutPath, changed: jsChanged },
    ],
    'Skill mastery badge outputs are up-to-date',
  );
}

main();
