#!/usr/bin/env node
import {
  finalizeGeneration,
  readJson,
  resolvePaths,
  writeIfChanged,
} from './lib/generator-file-utils.mjs';

const { projectRoot, configPath, rustOutPath, cssOutPath } = resolvePaths(import.meta.url, { configPath: ['config', 'animation-timings.json'], rustOutPath: ['rust', 'animation_timings_generated.rs'], cssOutPath: ['src', 'styles', 'generated-animation-timings.css'] });

function readTiming(timings, key) {
  const value = timings?.[key];
  if (!Number.isInteger(value) || value < 50) {
    throw new Error(`timings.${key} must be an integer >= 50`);
  }
  return value;
}

function generateRust({ floatHeartRemovalMs, sparkleRevealMs, jellyWobbleMs }) {
  return `// @generated by scripts/generate-animation-timings.mjs. Do not edit manually.\n\npub const FLOAT_HEART_REMOVAL_MS: i32 = ${floatHeartRemovalMs};\npub const SPARKLE_REVEAL_MS: i32 = ${sparkleRevealMs};\npub const JELLY_WOBBLE_MS: i32 = ${jellyWobbleMs};\n`;
}

function generateCss({ floatHeartRemovalMs, sparkleRevealMs, jellyWobbleMs }) {
  return `/* @generated by scripts/generate-animation-timings.mjs. Do not edit manually. */\n:root {\n  --timing-float-heart-removal: ${floatHeartRemovalMs}ms;\n  --timing-sparkle-reveal: ${sparkleRevealMs}ms;\n  --timing-jelly-wobble: ${jellyWobbleMs}ms;\n}\n`;
}

function main() {
  const config = readJson(configPath);
  const timings = config?.timings;
  const values = {
    floatHeartRemovalMs: readTiming(timings, 'float_heart_removal_ms'),
    sparkleRevealMs: readTiming(timings, 'sparkle_reveal_ms'),
    jellyWobbleMs: readTiming(timings, 'jelly_wobble_ms'),
  };

  const rustChanged = writeIfChanged(rustOutPath, generateRust(values));
  const cssChanged = writeIfChanged(cssOutPath, generateCss(values));

  finalizeGeneration(projectRoot, [
    { filePath: rustOutPath, changed: rustChanged },
    { filePath: cssOutPath, changed: cssChanged },
  ], 'Animation timing outputs are up-to-date');
}

main();
