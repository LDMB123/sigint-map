wait "$RUN_PID"
RUN_STATUS=$?
trap - INT TERM
if [[ $RUN_STATUS -ne 0 ]]; then
  printf "\nBatch 2 quality-tuned run exited with status %s.\n" "$RUN_STATUS" >&2
  printf "Log: %s\n" "$BATCH2_LOG" >&2
  exit "$RUN_STATUS"
fi

