export FIRST_PRINCIPLES_DIRECTIVE_CAP="${FIRST_PRINCIPLES_DIRECTIVE_CAP:-10}"
export FIRST_PRINCIPLES_APPEND_RAW_PROMPT="${FIRST_PRINCIPLES_APPEND_RAW_PROMPT:-0}"
